    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        try {

    Connection conn = Koneksi.getKoneksi();

    String pelanggan = cbPelanggan.getSelectedItem().toString();
    String layanan = cbLayanan.getSelectedItem().toString();

    String idPelanggan = pelanggan.split(" - ")[0];
    String idLayanan = layanan.split(" - ")[0];

    String sql = "UPDATE tbl_pesanan SET id_pelanggan=?, id_layanan=?, mata_kuliah=?, deadline=?, total_harga=?, status=?, catatan=? WHERE id_pesanan=?";

    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setString(1, idPelanggan);
    pst.setString(2, idLayanan);
    pst.setString(3, matakuliah.getText());
    pst.setString(4, deadline.getText());
    pst.setString(5, totalharga.getText());
    pst.setString(6, cbStatus.getSelectedItem().toString());
    pst.setString(7, catatan.getText());
    pst.setString(8, idpesanan.getText());

    pst.executeUpdate();

    JOptionPane.showMessageDialog(null, "Data Pesanan Berhasil Diubah");

    tampilData();

} catch (Exception e) {

    JOptionPane.showMessageDialog(null, e);

}
    }//GEN-LAST:event_editActionPerformed

