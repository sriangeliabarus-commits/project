    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:

    try {

        Connection conn = Koneksi.getKoneksi();

        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("ID Pesanan");
        model.addColumn("Pelanggan");
        model.addColumn("Layanan");
        model.addColumn("Mata Kuliah");
        model.addColumn("Deadline");
        model.addColumn("Total Harga");
        model.addColumn("Status");
        model.addColumn("Catatan");

        String sql = "SELECT p.id_pesanan, pl.nama, l.nama_layanan, "
                + "p.mata_kuliah, p.deadline, p.total_harga, "
                + "p.status, p.catatan "
                + "FROM tbl_pesanan p "
                + "JOIN tbl_pelanggan pl ON p.id_pelanggan = pl.id_pelanggan "
                + "JOIN tbl_pelayanan l ON p.id_layanan = l.id_pelayanan "
                + "WHERE pl.nama LIKE ? "
                + "OR l.nama_layanan LIKE ? "
                + "OR p.mata_kuliah LIKE ?";

        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, "%" + caripesanan.getText() + "%");
        pst.setString(2, "%" + caripesanan.getText() + "%");
        pst.setString(3, "%" + caripesanan.getText() + "%");

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{

                rs.getString("id_pesanan"),
                rs.getString("nama"),
                rs.getString("nama_layanan"),
                rs.getString("mata_kuliah"),
                rs.getString("deadline"),
                rs.getString("total_harga"),
                rs.getString("status"),
                rs.getString("catatan")

            });

        }

        tblDataPesanan.setModel(model);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e);

    }

    }//GEN-LAST:event_cariActionPerformed

